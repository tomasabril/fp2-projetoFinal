        private void execButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_execButtonActionPerformed
		// botao executar
		ExecCmd cmd = new ExecCmd(cmdTxtField.getText());
		execCmd.add(cmd);
		cmd.start();
		updateTable();
        }//GEN-LAST:event_execButtonActionPerformed


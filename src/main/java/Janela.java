	public void updateTable() {
		listModel.clear();
		int i = 0;
		for (ExecCmd e : execCmd) {
			if (e.isAlive()) {
				listModel.addElement(e.getCmd());
				e.setIndex(i);
				i++;
			}
		}
	}

